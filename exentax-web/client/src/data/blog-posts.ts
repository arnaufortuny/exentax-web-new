export interface BlogPost {
  slug: string;
  title: string;
  excerpt: string;
  category: string;
  readTime: number;
  publishedAt: string;
  updatedAt?: string;
  metaTitle: string;
  metaDescription: string;
  /**
   * Short social-card variant of the description (≤110 chars). When present,
   * the blog post page passes it to <SEO> as ogDescription so LinkedIn / X /
   * WhatsApp link previews don't get truncated. Falls back to metaDescription
   * when omitted. Localized variants live in `blog-i18n/<lang>.ts`.
   */
  socialDescription?: string;
  /**
   * Persuasive Open Graph title (≤60 chars). When omitted, falls back to
   * `metaTitle`. Set explicitly per post by `scripts/seo/inject-blog-og.mjs`
   * so every social card has a unique short headline.
   */
  ogTitle?: string;
  /**
   * Persuasive Open Graph description (120–160 chars). When omitted, the
   * blog post page falls back to `socialDescription` and finally
   * `metaDescription`. Generated per post by
   * `scripts/seo/inject-blog-og.mjs` so every social card meets the length
   * window required by Facebook / LinkedIn / X.
   */
  ogDescription?: string;
  /**
   * Optional per-article social card image (1200x630). When omitted, the
   * SEO component falls back to the default Exentax brand card at
   * `/og-image.png`. Must be a public path served from `client/public/`.
   */
  ogImage?: string;
  keywords?: string[];
  countries?: BlogCountryCode[];
  relatedSlugs?: string[];
}

