export function getBlogPost(slug: string): BlogPost | undefined {
  return BLOG_POSTS.find(p => p.slug === slug);
}

function normalizeKeyword(k: string): string {
  return k.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "").trim();
}

export interface RelatedOptions {
  excludeSlugs?: ReadonlyArray<string>;
}

export function getRelatedPosts(
  post: BlogPost,
  isAvailable: (slug: string) => boolean,
  limit: number = 3,
  options: RelatedOptions = {},
): BlogPost[] {
  const exclude = new Set(options.excludeSlugs ?? []);
  const available = (s: string) => !exclude.has(s) && isAvailable(s);

  const overrides: BlogPost[] = [];
  if (post.relatedSlugs && post.relatedSlugs.length > 0) {
    for (const s of post.relatedSlugs) {
      const p = BLOG_POSTS.find(bp => bp.slug === s);
      if (p && p.slug !== post.slug && available(p.slug)) overrides.push(p);
    }
    if (overrides.length >= limit) return overrides.slice(0, limit);
  }
  const used = new Set(overrides.map(p => p.slug));
  const fallback = scoreRelated(post, available).filter(p => !used.has(p.slug));
  return [...overrides, ...fallback].slice(0, limit);
}

function scoreRelated(post: BlogPost, isAvailable: (slug: string) => boolean): BlogPost[] {
  const myKw = new Set((post.keywords || []).map(normalizeKeyword));
  const sourceTime = new Date(post.publishedAt).getTime();
  const scored = BLOG_POSTS
    .filter(p => p.slug !== post.slug && isAvailable(p.slug))
    .map(p => {
      let score = 0;
      if (p.category === post.category) score += 3;
      const theirKw = (p.keywords || []).map(normalizeKeyword);
      for (const kw of theirKw) {
        if (myKw.has(kw)) score += 2;
        else {
          for (const mine of myKw) {
            if (kw && mine && (kw.includes(mine) || mine.includes(kw))) {
              score += 1;
              break;
            }
          }
        }
      }
      // Country overlap reinforces audience relevance.
      if (post.countries && p.countries) {
        const overlap = p.countries.some(c => post.countries!.includes(c));
        if (overlap) score += 1;
      }
      // Recency bonus: posts within ~60 days of the source get a small boost.
      const theirTime = new Date(p.publishedAt).getTime();
      if (!Number.isNaN(theirTime) && !Number.isNaN(sourceTime)) {
        const daysApart = Math.abs(theirTime - sourceTime) / 86_400_000;
        if (daysApart <= 60) score += 1;
      }
      return { post: p, score, time: theirTime };
    })
    .sort((a, b) => {
      if (b.score !== a.score) return b.score - a.score;
      // Tiebreaker: newer first, then shorter read.
      if ((b.time || 0) !== (a.time || 0)) return (b.time || 0) - (a.time || 0);
      return a.post.readTime - b.post.readTime;
    });
  return scored.map(s => s.post);
}

